package day03;

public class OperatorPractice2 {
    public static void main(String[] args) {
        int score = 80;

        // 1. 증감 연산자를 사용하여 score의 값을 1 증가시키고 출력하세요.
        System.out.println(score++);
        // 2. 복합 대입 연산자를 사용하여 증가된 score의 값에 9를 더하고 출력하세요.
        score+=9;
        System.out.println(score);

    }
}